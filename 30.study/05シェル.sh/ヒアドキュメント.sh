■■■■■■■■■■■■■■■■■■■■■■■■■■ 改行込で cat できる
cat <<EOF
line1
line2
line3
EOF

# EOF から次の EOF までがヒアドキュメントの内容
■■■■■■■■■■■■■■■■■■■■■■■■■■ ファイルを作成する場合
cat <<EOF > test
line1
line2
line3
EOF



■■■■■■■■■■■■■■■■■■■■■■■■■■ 詳細
・ヒアドキュメントは標準入力である。
・ヒアドキュメントは文字列リテラルではない。
・EOF は、任意の文字列でかまわない
・変数を文字列として標準入力したい場合
 →'EOF' クオートしておく
・tab を入れたい場合
 → <<-EOF でヒアドキュメントを書く
  →\t\tline2 みたく、タブは表現する
